// Resuelve un campo multilingüe { es, en, … } al idioma activo.
export function loc(field, lang) {
  if (field == null) return '';
  if (typeof field === 'string') return field;
  const base = String(lang || 'es').split('-')[0];
  return field[base] || field.en || field.es || '';
}

