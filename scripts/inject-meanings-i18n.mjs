// Overrides por (lección|carácter) para caracteres polisémicos según contexto.
const OVERRIDE = {
  // 分 = minuto en L4, pero unidad de moneda (céntimo/fen) en L7.
  '7|分': tr('fen (unit of one cent)', 'fen (unité d’un centime)', 'Fen (Einheit von einem Cent)', 'fen (unità di un centesimo)', 'fen (unidade de um centavo)'),
};

// Variantes de clave para caracteres con paréntesis de ancho completo/medio,
// por si el dato usa una forma distinta a la del mapa.
const KEY_ALIASES = {
  '(一)点儿': ['（一）点儿'],
  '块（钱）': ['块(钱)'],
  '有（一）点儿': ['有(一)点儿'],
  '点（钟）': ['点(钟)'],
};
for (const [canonical, aliases] of Object.entries(KEY_ALIASES)) {
  if (TR[canonical]) for (const a of aliases) if (!TR[a]) TR[a] = TR[canonical];
}

const data = JSON.parse(readFileSync(FILE, 'utf8'));
const missing = [];
let count = 0;
for (const lesson of data.lessons) {
  for (const w of lesson.words) {
    const ov = OVERRIDE[`${lesson.lesson}|${w.char}`];
    const t = ov || TR[w.char];
    if (t) { w.meaningTr = t; count++; }
    else missing.push(`${lesson.lesson}:${w.char}`);
  }
}

writeFileSync(FILE, JSON.stringify(data, null, 2) + '\n', 'utf8');
console.log(`meaningTr inyectado en ${count} palabras.`);
if (missing.length) {
  console.log(`\n⚠ SIN traducción (${missing.length}):`);
  console.log(missing.join('\n'));
} else {
  console.log('✓ Todas las palabras tienen traducción.');
}
